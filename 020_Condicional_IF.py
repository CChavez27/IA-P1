#Declaramos nuestras variables con sus respectivos valores
a = 27
b = 9

#Realizamos nuestras comparaciones
if a == b:
    print('El valor del dato a y b son iguales')

if a != b:
    print('El valor del dato a y b son diferentes')

if a > b:
    print('El valor del dato a es mayor que el dato b')
  
if a < b:
    print('El valor del dato b es mayor que el dato a')

if a >= b:
    print('El valor del dato a es mayor o igual que el dato b')
  
if a <= b:
    print('El valor del dato b es mayor o igual que el dato a')
